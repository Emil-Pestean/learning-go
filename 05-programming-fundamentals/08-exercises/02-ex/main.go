package main

import "fmt"

// Hands-on exercise #2
// Using the following operators, write expressions and assign their values to variables:
// ==
// <=
// >=
// !=
// <
// >
// Now print each of the variables.

func main() {

	x := 1 == 2
	y := 1 <= 2
	z := 1 >= 2
	b := 1 != 2
	n := 1 > 2
	m := 1 < 2

	fmt.Println(x)
	fmt.Println(y)
	fmt.Println(z)
	fmt.Println(b)
	fmt.Println(n)
	fmt.Println(m)

}
